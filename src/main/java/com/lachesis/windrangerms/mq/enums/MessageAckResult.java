package com.lachesis.windrangerms.mq.enums;

/**
 * 消息确认结果枚举
 * 用于消费者方法返回，指示消息的处理结果和后续操作
 */
public enum MessageAckResult {
    
    /**
     * 消费成功，确认消息（ACK）
     * 消息将被标记为已处理，不会再次投递
     */
    SUCCESS,
    
    /**
     * 消费失败，需要重试
     * 消息将根据重试策略进行重新投递
     * 如果超过最大重试次数，将进入死信队列
     */
    RETRY,
    
    /**
     * 拒绝消息，直接进入死信队列
     * 消息不会重试，直接被标记为失败并进入死信队列
     */
    REJECT,
    
    /**
     * 忽略消息，确认但不处理
     * 消息将被确认（ACK），但业务逻辑可能没有完全处理
     * 适用于某些特殊场景，如消息格式不正确但不影响系统运行
     */
    IGNORE
}